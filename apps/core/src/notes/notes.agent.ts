import type { AgentSpec, AgentTool } from "../agents/agent.types";

const NOTES_AGENT_SYSTEM_PROMPT = `\
Ты — агент заметок Веги. Ты получаешь задачу от супервизора и решаешь, что делать с продиктованным пользователем текстом.

Возможные tool'ы:
- save_short_note(text): сохрани короткую заметку (одно-два предложения) и подтверди звуком ack_done.
- enable_long_note_mode(intent): переключи сессию в long-note режим — silence cap уйдёт на 60s, Ear VAD умолкнет, сыграет Submarine. Вызывай ТОЛЬКО когда пользователь явно намерен надиктовать длинную заметку ("запиши длинную заметку", "запиши заметку про...", "сейчас наговорю").
- end_long_note_mode(cleanText): сохрани полностью продиктованный текст длинной заметки и заверши сессию обычным endpoint. Передавай cleanText без триггерной фразы "конец заметки" / "стоп".

Правила:
1. Если задача — переключение в long-note — используй enable_long_note_mode.
2. Если задача — короткая заметка — используй save_short_note сразу.
3. Если задача — завершить длинную заметку — используй end_long_note_mode с очищенным текстом.
4. Никогда не выдумывай содержимое заметок. Текст должен браться из задачи / истории.
5. Финальный ответ — одна короткая фраза-summary на русском.`;

const NOTES_EXAMPLES = [
  "вега запиши заметку купить молоко",
  "вега запиши длинную заметку про идею проекта",
  "конец заметки",
  "сохрани вот это: ...",
];

export function buildNotesAgentSpec(tools: AgentTool[]): AgentSpec {
  return {
    name: "notes",
    description:
      "Сохранение заметок: короткие сразу на диск; для длинных — переключает сессию в long-note режим и завершает её по сигналу пользователя.",
    examples: NOTES_EXAMPLES,
    systemPrompt: NOTES_AGENT_SYSTEM_PROMPT,
    tools,
    enabled: true,
  };
}
